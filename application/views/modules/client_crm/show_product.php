function change_status(product_id, is_active) {
    if (is_active == 1)
        status = 0;
    else if (is_active == 0) {
        status = 1;
    }
    var ops_url = baseurl + 'product/change_status/';
    $.ajax({
        type: "POST",
        cache: false,
        async: false,
        url: ops_url,
        data: {
            "load": 1,
            "product_id": product_id,
            "status": status
        },
        success: function(result) {
            var data = $.parseJSON(result);

            if (data.status == 1) {
                if (status == 0) {
                    Swal.fire({
                        title: 'Success',
                        text: 'Product status deactivated.',
                        icon: 'success',
                        confirmButtonText: 'OK'
                    }).then((result) => {
                        if (result.isConfirmed) {
                            //location.reload();
                        }
                    });
                } else if (status == 1) {
                    Swal.fire({
                        title: 'Success',
                        text: 'Product status activated.',
                        icon: 'success',
                        confirmButtonText: 'OK'
                    }).then((result) => {
                        if (result.isConfirmed) {
                           // location.reload();
                        }
                    });
                }
            }
        }
    });
}

function edit_product(product_id,) {
    var ops_url = baseurl + 'product/edit-product';
    $.ajax({
        type: "POST",
        cache: false,
