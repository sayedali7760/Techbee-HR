	function create_live() {
		var ops_url = baseurl + 'mt-account/create-live';
		$.ajax({
			type: "POST",
			cache: false,
			async: false,
			url: ops_url,
			data: {
				"load": 1,
			},
			success: function(result) {
				var data = $.parseJSON(result);
				$("#create_mtl_div").html(data.view);
				$('#create_mtl_div').addClass('in-down');
				$("html, body").animate({
					scrollTop: 0
				}, "slow");
			}
		});
	}

